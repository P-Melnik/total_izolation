package pmelnik.local.leetcode_easy;

/**
 * Краткое описание решения
 * Проходим по массиву цифр с конца, прибавляем 1 и обрабатываем перенос.
 * Если после обработки всех цифр остался перенос,
 * создаем новый массив с дополнительной цифрой в начале.
 * ---
 * Анализ сложности
 * Время: O(n), где n - длина массива (в худшем случае проходим весь массив)
 * Память: O(1) в лучшем случае, O(n) в худшем (когда нужно увеличить размер массива)
 */
public class PlusOneNum66 {

    public int[] plusOne(int[] digits) {
        int n = digits.length;

        for (int i = n - 1; i >= 0; i--) {
            if (digits[i] < 9) {
                digits[i]++;
                return digits;
            }
            digits[i] = 0;
        }

        // Если дошли сюда, значит все цифры были 9 (например, 999 -> 1000)
        int[] newDigits = new int[n + 1];
        newDigits[0] = 1;
        return newDigits;
    }
}
